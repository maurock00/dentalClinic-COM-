    public void setDatosTablaDientes()
    {
        DefaultTableModel modelo = (DefaultTableModel) tablaDientes.getModel();
        modelo.setRowCount(0);
        dienteTMP = new Diente(this.codigoHistoriaClinica);
        try {
            dientes = dienteTMP.consultarDientes();  
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(VentanaDiente.class.getName()).log(Level.SEVERE, null, ex);
        }
            for(int i=0;i<dientes.size();i++)
            {
                modelo.addRow(new Object[]{dientes.get(i).getIndiceDiente(),dientes.get(i).getDiagnosticoDiente(),
                    dientes.get(i).getTratamientoDiente(),dientes.get(i).getFechaRevisionDiente()});
            }
    }
    

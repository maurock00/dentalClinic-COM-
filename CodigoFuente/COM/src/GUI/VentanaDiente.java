    private void botonAtctualizarDienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAtctualizarDienteActionPerformed
        dienteTMP = new Diente(codigoHistoriaClinica,""+piezaDental.getSelectedItem());
        java.util.Date fechaJava = new Date();
        java.sql.Date fechaSQL = new java.sql.Date(fechaJava.getTime());
        try {
            if(dienteTMP.verificarDiente())
            {
                dienteTMP.setCodigoHistoriaClinica(codigoHistoriaClinica);
                dienteTMP.setIndiceDiente(""+piezaDental.getSelectedItem());
                dienteTMP.setDiagnosticoDiente(guiDiagnosticoDiente.getText());
                dienteTMP.setTratamientoDiente(guiTratamientoDiente.getText());
                dienteTMP.setFechaRevisionDiente(fechaSQL);
                dienteTMP.setCaraA(getCaraA());
                dienteTMP.setCaraB(getCaraB());
                dienteTMP.setCaraC(getCaraC());
                dienteTMP.setCaraD(getCaraD());
                dienteTMP.setCaraE(getCaraE());

                dienteTMP.actualizarDiente();
                guiFechaDiente.setText(""+fechaSQL);
                this.setDatosTablaDientes();
            }
            else
            {
                dienteTMP.setCodigoHistoriaClinica(codigoHistoriaClinica);
                dienteTMP.setIndiceDiente(""+piezaDental.getSelectedItem());
                dienteTMP.setDiagnosticoDiente(guiDiagnosticoDiente.getText());
                dienteTMP.setTratamientoDiente(guiTratamientoDiente.getText());
                dienteTMP.setFechaRevisionDiente(fechaSQL);
                dienteTMP.setCaraA(getCaraA());
                dienteTMP.setCaraB(getCaraB());
                dienteTMP.setCaraC(getCaraC());
                dienteTMP.setCaraD(getCaraD());
                dienteTMP.setCaraE(getCaraE());

                dienteTMP.ingresarDiente();
                guiFechaDiente.setText(""+fechaSQL);
                this.setDatosTablaDientes();
            }
        }
        catch (ClassNotFoundException ex){
            throw new ClassCastException(ex.getMessage());
        }
    }//GEN-LAST:event_botonAtctualizarDienteActionPerformed


    private void piezaDentalItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_piezaDentalItemStateChanged
        String indiceAux = ""+piezaDental.getSelectedItem();
        dienteTMP = new Diente(codigoHistoriaClinica,indiceAux);
        try 
        {
            if(dienteTMP.verificarDiente())
            {
                dienteTMP = dienteTMP.consultarDiente();
                guiFechaDiente.setText(""+dienteTMP.getFechaRevisionDiente());
                guiDiagnosticoDiente.setText(dienteTMP.getDiagnosticoDiente());
                guiTratamientoDiente.setText(dienteTMP.getTratamientoDiente());
                setCaraA(dienteTMP.getCaraA());
                setCaraB(dienteTMP.getCaraB());
                setCaraC(dienteTMP.getCaraC());
                setCaraD(dienteTMP.getCaraD());
                setCaraE(dienteTMP.getCaraE());
            }
            else
            {
                guiFechaDiente.setText("");
                guiDiagnosticoDiente.setText("");
                guiTratamientoDiente.setText("");
                setCaraA(0);
                setCaraB(0);
                setCaraC(0);
                setCaraD(0);
                setCaraE(0);
            }
        }catch (ClassNotFoundException ex) {
            throw new ClassCastException(ex.getMessage());
        }
    }//GEN-LAST:event_piezaDentalItemStateChanged

